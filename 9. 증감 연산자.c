#include <stdio.h>

int main(){
    int var = 100;
    
    printf("%d\n", var);
    
    printf("%d\n", var--);
    
    printf("%d\n", var);
    
    printf("%d\n", --var);
    
    printf("%d\n", var);
    
    printf("---\n");
    
    printf("%d\n", var);
    
    printf("%d\n", var++);
    
    printf("%d\n", var);
    
    printf("%d\n", ++var);
    
    printf("%d\n", var);
    
    return 0;
}